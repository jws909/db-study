SELECT
    COUNT(EMPNO),
    COUNT(ENAME),
    COUNT(MGR),
    COUNT(COMM) -- NULL이 아닌 값의 갯수
FROM EMP;

-- NULL값 제외 값을 셀때 권장되는 방법
SELECT
    COUNT(*)
FROM EMP
WHERE COMM IS NOT NULL;

SELECT
    COUNT(*)
FROM EMP
WHERE COMM IS NULL;

