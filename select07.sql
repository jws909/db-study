-- 1,2,3 학년별 평균키 학년기준 내림차순 정렬
SELECT
    GRADE 학년,
    AVG(HEIGHT) 평균키
FROM STUDENT
WHERE GRADE IN (1,2,3)
GROUP BY GRADE
ORDER BY 학년 DESC;


1) 급여가 2000 이상인 직원들 대상으로 부서별 평균급여
SELECT
    DEPTNO,
    AVG(SAL)
FROM EMP
WHERE SAL >= 2000
GROUP BY DEPTNO
ORDER BY DEPTNO;

2) 부서별 평균급여가 2000 이상인 부서별 평균급여
SELECT
    DEPTNO,
    AVG(SAL)
FROM EMP
GROUP BY DEPTNO
HAVING AVG(SAL) >= 2000;


STUDENT
학생 각 학년별 평균 몸무게
평균 몸무게가 65Kg 이상 경우만
SELECT 
    GRADE, AVG(WEIGHT)
FROM STUDENT
GROUP BY GRADE
HAVING AVG(WEIGHT) >= 65;


STUDENT
4학년을 제외하고
학생 각 학년별 평균 몸무게
평균 몸무게가 65Kg 이상 경우만
학년별 오름차순 정렬
SELECT
    GRADE,
    AVG(WEIGHT)
FROM STUDENT
WHERE GRADE <> 4
GROUP BY GRADE
HAVING AVG(WEIGHT) >= 65
ORDER BY GRADE;


