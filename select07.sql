수치집계
SUM / AVG / MAX / MIN / STDDEV / VARIANCE

-- 총 직원 몇명, 총급여 얼마냐
SELECT
    COUNT(*), SUM(SAL), SUM(COMM)
FROM EMP;

-- 10, 20 부서에 다니는 직원 몇명, 총급여
SELECT
    COUNT(*), SUM(SAL)
FROM EMP
WHERE DEPTNO IN (10, 20);

SELECT
    COUNT(HEIGHT),
    SUM(HEIGHT),
    AVG(HEIGHT),
    MAX(HEIGHT),
    MIN(HEIGHT),
    STDDEV(HEIGHT),
    VARIANCE(HEIGHT),
    AVG(WEIGHT)
FROM STUDENT;


