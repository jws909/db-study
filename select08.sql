GROUP BY ROLLUP(DEPTNO, JOB)
1) GROUP BY DEPTNO, JOB 그룹화 데이터 집계
2) GROUP BY DEPTNO 그룹화 데이터 집계
3) () 그룹화 데이터 집계 -> 전체집계

GROUP BY ROLLUP(JOB, DEPTNO)
1) GROUP BY JOB, DEPTNO 그룹화 데이터 집계
2) GROUP BY JOB 그룹화 데이터 집계
3) () 그룹화 데이터 집계 -> 전체집계

GROUP BY ROLLUP((DEPTNO, JOB))
1) GROUP BY (DEPTNO, JOB) 그룹화 데이터 집계
2) () 그룹화 데이터 집계 -> 전체집계



