집계

--EMP 부서와 직업별 평균 급여와 사원수

SELECT DEPTNO, JOB, AVG(SAL) 평균급여, COUNT(*) 사원수
FROM EMP
GROUP BY DEPTNO, JOB;

--EMP 부서별 평균 급여와 사원수
SELECT DEPTNO, AVG(SAL) 평균급여, COUNT(*) 사원수
FROM EMP
GROUP BY DEPTNO;

--EMP 전체 사원의 평균 급여와 사원수
SELECT AVG(SAL), COUNT(*)
FROM EMP;

--위 3개의 집계 내역을 하나로 통함

SELECT DEPTNO, JOB, AVG(SAL) 평균급여, COUNT(*) 사원수
FROM EMP
GROUP BY DEPTNO, JOB
UNION ALL
SELECT DEPTNO, NULL, AVG(SAL), COUNT(*)
FROM EMP
GROUP BY DEPTNO
UNION ALL
SELECT NULL, NULL, AVG(SAL), COUNT(*)
FROM EMP
ORDER BY DEPTNO, 2;


