SELECT DEPTNO, MAX(SAL)
FROM EMP
GROUP BY DEPTNO;


SELECT *
FROM EMP A, (SELECT DEPTNO, MAX(SAL) MAX_SAL
             FROM EMP
             GROUP BY DEPTNO) B
WHERE A.DEPTNO = B.DEPTNO;

--부서별 최대 급여 + 부서명
SELECT DEPTNO, MAX(SAL)
FROM EMP
