ROWNUM 활용

SELECT ROWNUM, RN, STUDNO, NAME, HEIGHT
FROM(
        SELECT
            ROWNUM RN,
            CEIL(ROWNUM/3),
            STUDNO,
            NAME,
            HEIGHT
        FROM STUDENT
        ORDER BY HEIGHT);
        

SELECT *
FROM STUDENT
WHERE ROWNUM <= 5;  --그냥 조회 결과 5명

--키큰 사람 5명 조회
SELECT *
FROM STUDENT
WHERE ROWNUM <= 5       --기존배정된 ROWNUM으로 인식 -> 정렬 이후 ROWNUM과 다름
ORDER BY HEIGHT DESC;

SELECT ROWNUM, STUDNO, NAME, HEIGHT
FROM (SELECT *
      FROM STUDENT
      ORDER BY HEIGHT DESC)
WHERE ROWNUM <= 5;



--팀 번호로 팀 조회
SELECT
    ROWNUM,
    CEIL(ROWNUM/3),
    STUDNO,
    NAME
FROM STUDENT
WHERE CEIL(ROWNUM/3) = 3;   --인식 제대로 안됨

SELECT *
FROM(SELECT
        ROWNUM RN,
        CEIL(ROWNUM/3) TEAM,
        STUDNO,
        NAME
     FROM STUDENT)
WHERE TEAM = 3;


