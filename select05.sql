타입변환

TO_CHAR     문자로
TO_DATE     날짜로
TO_NUMBER   숫자로

SELECT 2+2 FROM DUAL;
SELECT 2+'2' FROM DUAL; --자동형변환(묵시적형변환)
SELECT 2||'2' FROM DUAL;
SELECT 2+TO_NUMBER('2') FROM DUAL;  --명시적형변환

SELECT
    TO_CHAR(12341),
    TO_CHAR(123.503),
    SYSDATE,
    TO_CHAR(SYSDATE, 'YYYY') YYYY,    -- Y M D
    TO_CHAR(SYSDATE, 'YY') YY,
    TO_CHAR(SYSDATE, 'MM') MM,
    TO_CHAR(SYSDATE, 'DD') DD,
    TO_CHAR(SYSDATE, 'YYYYMMDD') YYYYMMDD,
    TO_CHAR(SYSDATE, 'YYMMDD') YYMMDD,
    TO_CHAR(SYSDATE, 'YY/MM/DD') "YY/MM/DD",
    TO_CHAR(SYSDATE, 'YYYY-MM-DD') "YYYY-MM-DD"
FROM DUAL;

--JUMIN 문자타입 -> SUBSTR
--BIRTHDAY 날짜타입 -> TO_CHAR
SELECT
    JUMIN,
    BIRTHDAY,
    TO_CHAR(BIRTHDAY, 'YY') 년,
    TO_CHAR(BIRTHDAY, 'MM') 월,
    TO_CHAR(BIRTHDAY, 'DD') 일,
    TO_CHAR(BIRTHDAY, 'YYYY-MM-DD') "YYYY-MM-DD"
FROM STUDENT;

SELECT
    SYSDATE,
    TO_CHAR(SYSDATE, 'YYYY-MM-DD HH24:MI:SS'),
    SYSTIMESTAMP,
    TO_CHAR(SYSTIMESTAMP, 'YYYY-MM-DD HH24:MI:SS')
FROM DUAL;

SELECT
    TO_NUMBER('12313'),
    TO_NUMBER('123.343')
    -- TO_NUMBER('13929adf')    오류발생
FROM DUAL;

SELECT
    TO_CHAR(1234, '999999') "1",
    TO_CHAR(1234, '0999999') "2",
    TO_CHAR(1234, '$999999') "3",
    '$' || 1234 "4",
    TO_CHAR(1234, '9999.99') "5",
    TO_CHAR(12345678, '999,999,999') "6"
FROM DUAL;


SELECT
    --'2025-12-02' + 3
    --'2025-12-02' || 3
    TO_DATE('2025-12-02') + 3 "1",
    TO_DATE('2025/12/02') + 5 "2",
    TO_DATE('25/12/02') + 7 "3",
    TO_DATE('20251202') + 9 "4",
    LAST_DAY('2024-02-03') "5",
    LAST_DAY(TO_DATE('2024-02-03')) "6",
    TO_DATE('2025-03-04', 'YYYY-MM-DD') "7",
    TO_DATE('25/03/04', 'YY/MM/DD') "8",
    TO_DATE('2025,03,04', 'YYYY,MM,DD') "9"
FROM DUAL;


SELECT
    '2026-03-23' "1",
    SUBSTR('2026-03-23', 6, 2) "2",
    TO_CHAR(TO_DATE('2026-03-23'), 'MM') 월추출
FROM DUAL;